package ch03_op;
/*
 * 산술 연산
 *  / : 나누기 연산자
 *      정수 = 정수/정수
 *  % : 나머지 연산자
 *      나머지연산의 결과값의 부호 피쳇수의 부호와 같다.
 */

public class OpEx6 {

	public static void main(String[] args) {
		System.out.println(10/8); 
		System.out.println(10/-8); 
		System.out.println(-10/8); 
		System.out.println(-10/-8); 
		
		System.out.println(10%8); 
		System.out.println(10%-8); 
		System.out.println(-10%8); 
		System.out.println(-10%-8);
		
	}

}
